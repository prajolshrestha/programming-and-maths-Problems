# 1. Numbers: numeric types, including integers (int), floating-point numbers (float), and complex numbers (complex).
my_int = 42
my_float = 3.14
my_complex = 1 + 2j

# 2. Strings: Strings are sequences of characters and are used for text manipulation.
#            Strings are immutable
my_string = "Hello, World!"

# 3. Lists: list is an ordered collection of elements, and it can contain elements of different data types.
#           Lists are mutable, which means you can change their content (add, remove, or modify elements).
my_list = [1, 2, 3, "Python", True]

# 4. Tuples:  tuple is an ordered collection of elements similar to a list, 
#             but tuples are immutable, meaning their elements cannot be modified after creation.
my_tuple = (1, 2, 3, "Tuple", False)

# 5. Dictionary: A dictionary is an unordered collection of key-value pairs.
#                Each key in a dictionary is unique, and you can use it to access the associated value.
#
#               Dictionaries are mutable, and keys must be immutable (e.g., strings, numbers).
my_dict = {"name": "Alice", "age": 30, "city": "New York"}

# 6. Sets: A set is an unordered collection of unique elements. 
#           Sets are mutable, but their elements must be immutable (e.g., numbers, strings).
my_set = {1, 2, 3, 3, 4}
